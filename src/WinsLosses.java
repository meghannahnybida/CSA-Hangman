public class WinsLosses {

    private int wins;
    private int losses;

    public int getWins(){
        return wins;
    }
    public void setWins(int winsAmount){
        wins = winsAmount;
    }
    public int getLosses(){
        return losses;
    }
    public void setLosses(int lossesAmount){
        losses = lossesAmount;
    }

}